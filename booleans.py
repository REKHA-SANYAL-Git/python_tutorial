# Booleans represent one of two values: True or False.

# comparison between two values
print(10 > 9)
print(10 == 9)
print(10 < 9)

# comparison between two values stored in their respective variables
a = 200
b = 33

if b > a:
  print("b is greater than a")
else:
  print("b is not greater than a")

# evalute string and value
print(bool("Hello"))
print(bool(15))

# evalute string and value stored in their respective variables
x = "Hello"
y = 15

print(bool(x))
print(bool(y))
